//////////////////////////////////////////////////////////////////////////// Header Files

#include <iostream>
#include "Section.hpp"

////////////////////////////////////////////////////////////////////////////// Namespaces 

using namespace std;

//////////////////////////////////////////////////////////////////////////// Constructors

Section::Section() {
	sectionNumber = "N/A";
}

///////////////////////////////////////////////////////////////////////////////// Getters

string Section::getSectionNumber() const {
	return sectionNumber;
}

vector<Books> Section::getBooksList() const {
	return listOfBooks;
}

///////////////////////////////////////////////////////////////////////////////// Setters

void Section::setSectionNumber(string newSectionNumber) {
	sectionNumber = newSectionNumber;
}

///////////////////////////////////////////////////////////// Non-Setter/Getter Functions

void Section::pushToBooksList(Books newBook) {
	listOfBooks.push_back(newBook);
}

//////////////////////////////////////////////////////////////////// Overloaded Functions

ostream& operator<<(ostream& out, const Section section) {
	
	out << "(Section number: " << section.getSectionNumber() << ")"; 
	
	return out;
}